package homework_40;

public class StudentTask1 {
        private String name;
        private int age;
        private double avgGrade;

    public StudentTask1(String name, int age, double avgGrade) {
        this.name = name;
        this.age = age;
        this.avgGrade = avgGrade;
    }

    public String getName() {
            return name;
        }

        public int getAge() {
            return age;
        }

        public double getAvgGrade() {
            return avgGrade;
        }
    }



