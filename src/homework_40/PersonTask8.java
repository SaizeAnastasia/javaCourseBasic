package homework_40;

public class PersonTask8 {
        private String name;
        private int age;
        private String gender;
        private int salary;

    public PersonTask8(String name, int age, String gender, int salary) {
        this.name = name;
        this.age = age;
        this.gender = gender;
        this.salary = salary;
    }

    public String getGender() {
            return gender;
        }

        public int getAge() {
            return age;
        }

        public int getSalary() {
            return salary;
        }
    }

